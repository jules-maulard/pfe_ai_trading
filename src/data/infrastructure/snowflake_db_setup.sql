-- Create database, schema, tables
USE ROLE SYSADMIN;
USE WAREHOUSE COMPUTE_WH;

CREATE DATABASE IF NOT EXISTS TRADING_AI COMMENT = 'PFE AI Trading';
DROP SCHEMA IF EXISTS TRADING_AI.PUBLIC;
CREATE SCHEMA IF NOT EXISTS TRADING_AI.MARKET_DATA;

USE DATABASE TRADING_AI;
USE SCHEMA TRADING_AI.MARKET_DATA;

CREATE TABLE IF NOT EXISTS TRADING_AI.MARKET_DATA.OHLCV (
    SYMBOL VARCHAR(20) NOT NULL,
    DATE DATE NOT NULL,
    OPEN FLOAT,
    HIGH FLOAT,
    LOW FLOAT,
    CLOSE FLOAT,
    VOLUME BIGINT,
    PRIMARY KEY (SYMBOL, DATE)
);

CREATE TABLE IF NOT EXISTS TRADING_AI.MARKET_DATA.ASSET (
    SYMBOL VARCHAR(20) NOT NULL PRIMARY KEY,
    COMPANY_NAME VARCHAR(500),
    SECTOR VARCHAR(200),
    INDUSTRY VARCHAR(200),
    CURRENCY VARCHAR(10),
    COUNTRY VARCHAR(100),
    EXCHANGE VARCHAR(50),
    LONG_BUSINESS_SUMMARY VARCHAR(16000),
    WEBSITE VARCHAR(500)
);

CREATE TABLE IF NOT EXISTS TRADING_AI.MARKET_DATA.DIVIDEND (
    SYMBOL VARCHAR(20) NOT NULL,
    DATE DATE NOT NULL,
    AMOUNT FLOAT,
    PRIMARY KEY (SYMBOL, DATE)
);

